# Calificacion Alummno dice si esta eximido o no

calificacion = float (input('Ingrese la calificacion de un Alummno '))
if calificacion >= 6.0:
    print ('Felicitaciones, estas eximido, ya que tu calificacion es igual o superior a a 6,0!!')
elif calificacion >= 4.0:
    print ('Felicitaciones, estas eximido, ya que tu calificacion es igual o superior a a 4,0!!')
else:
    print ('¡Lo siento, reprobaste, tu calificacion es igual o inferior a 4,0¡')
    
