# Rango de asistencia

asistencia = int (input('Cual es tu promedio de asistencia '))
if asistencia >= 100:
    print ('Tu asistencia es completa vas bien!!')
elif asistencia >= 70:
    print ('Tu asistencia es relativamente buena!!')
elif asistencia >= 50:
    print ('Tu asistencia es de la mitad NO FALTES MAS')
else:
    print ('TU ASISTENCIA ES MALISIMA DEBES IR A DIRECCION')
    
