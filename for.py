#Bucle for range (inicio, fin, paso)

for numero in range (2,21,2): #Inicia en dos termina antes
    print(numero)
    
for numero in range (1,6,1): #Inicia en uno termina antes del 6 y suma 1
    print(numero)

#restar
for numero in range (10,0,-1): #10,9,8,7....
    print(numero)
    
#pasos grandes
for i in range (0,100,25): #10,9,8,7....
    print(i)